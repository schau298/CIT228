fruits=["strawberries", "oranges", "bannanas"]
for fruit in fruits:
    print("The fruit is: ",fruit)
    if fruit=="strawberries":
        print("They are red!")
    
    if fruit=="bannanas":
        print("They are yellow!")
    
    if fruit=="oranges":
        print("They are orange!")
    
    if fruit=="blueberries":
        print("No blueberries")
     
    if fruit=="raspberries":
        print("No raspberries")